        <p class="font-bold text-3xl">What is a blockchain?</p>
        <p class="mt-2">
          A blockchain is a decentralized record that gets its name from how it
          stores its data. Once a set of transaction data reaches a certain
          size, it forms a "block.” This is where every transaction on a
          blockchain is validated and then permanently stored. The “chain” part
          of a blockchain is a series of consecutive blocks linked together,
