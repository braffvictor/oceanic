        <p class="font-bold text-2xl mt-5">The Proof-of-Stake method</p>
        <p class="mt-2">
          Blockchains that use the Proof-of-Stake method verify transactions
          using validators. When a new transaction is potentially going to be
          added to the blockchain, a validator is randomly selected to be a
          block proposer. This validator will then create a new block and send
          it to the other nodes on the network. For each new slot, a committee
          of validators is chosen and it is their votes that determine the
          block's validity. An example of a blockchain that uses Proof-of-Stake
          is Ethereum.
        </p>
        <p class="font-bold text-2xl mt-5">The Proof-of-Work method</p>
        <p class="mt-2">
          Blockchains that use the Proof-of-Work method verify transactions
          using miners. Proof-of-Work blockchains use extreme computational
          output (the “work”) and consensus as barriers to bad actors writing
          invalid transactions to the blockchain. When a new transaction is
          potentially going to be added to the blockchain, a bunch of miners
          (rather, their computers) will race to solve a complex math equation.
          After someone solves it, several other miners will verify that their
          answer is correct. If everyone agrees that the fastest miner’s output
          is correct, the transaction will be recorded to the blockchain, and
          the miner will be awarded a fee for the energy used to complete the
          computation.
        </p>
        <p class="mt-2">
          With Proof-of-Work, not only do several miners need to agree on a
          transaction’s validity, but the barrier to entry for a bad actor is
          extremely high due to the computational work and energy required to
          validate a transaction. For example, there are only a set number of
          Bitcoin in existence. Therefore, in order to mine each subsequent
          coin, a more complex problem needs to be solved. This also makes
          transactions more expensive.
        </p>
        <p class="mt-2">
          Both of these methods are complex, time-consuming, and ultimately
          ensure the security of the blockchain, which is why the gas fees are
          awarded to the operators.
        </p>

        <p class="font-bold text-3xl mt-5">Gas fees</p>
