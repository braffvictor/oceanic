          opens a new deposit box, they receive a key unique to that box, but
          making a copy of the key does not duplicate the box's contents; it
          only provides access.
        </p>

        <p class="font-bold text-2xl mt-5">Trustless and permissionless</p>

        <p class="mt-2">
          The blockchain records and preserves history and acts as an unbiased
          party in transactions. For that reason, it’s “trustless” in that it
          doesn’t require you to put your trust in another organization or
          entity in order to transact. Similarly, because the transactions are
          carried out by a network of computers, they are “permissionless” in
          that they don’t require the permission of a third party.
        </p>

        <p class="font-bold text-3xl mt-5">Types of blockchains</p>

        <p class="mt-2">
          here are different types of blockchains, and each has varying gas fees
          and scalability.
        </p>

        <p class="font-bold text-2xl mt-5">Layer 1 blockchains</p>

        <p class="mt-2">
          Layer 1 (also called "L1") blockchains are the main, or base level,
          blockchains. This means they execute and validate transactions on
          their own, without assistance. Ethereum, Solana, and Avalanche are
          Layer 1 chains. All three of these L1 chains don't require help from
          other systems to run, and each has their own associated
          cryptocurrencies.
        </p>

        <p class="font-bold text-2xl mt-5">Layer 2 blockchains</p>

        <p class="mt-2">
          Layer 2 (or "L2") blockchains act as scaling solutions for Layer 1s.
          Layer 2 chains process transactions on their chain and then store a
          summary of the completed actions on the Layer 1 chain. Optimism and
          Arbitrum are considered Layer 2 "rollups" because their transactions
          occur off-chain and are then added to the Layer 1, or base, chain when
          completed. This significantly lowers the demands on the blockchain,
          improving scalability and lowering its gas fees.
        </p>

        <p class="font-bold text-2xl mt-5">Sidechains</p>

        <p class="mt-2">
          Finally, there is a type of chain called a "sidechain" which is a
          separate, independent blockchain that's linked to the Layer 1 chain
          via a two-way bridge. Sidechains, like Polygon, are designed to
          process transactions efficiently and are essentially self-contained
          blockchains responsible for their own security. What connects them to
          the main blockchain is their ability to transfer digital items to the
          main blockchain. In the case of Polygon, an Ethereum-compatible chain,
          users get an experience similar to using Ethereum but with potentially
          lower gas fees and faster transaction times.
        </p>
        <p class="font-bold text-3xl mt-5">Consensus mechanisms</p>

